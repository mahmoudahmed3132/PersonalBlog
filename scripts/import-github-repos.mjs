import fs from "node:fs/promises";
import path from "node:path";

const username = "mahmoudahmed3132";
const outputPath = path.join(process.cwd(), "src/lib/generated-projects.ts");
const excludedRepos = new Set(["personal-blog", "mywebsite", "mypersonalwebsite", "pdfreader"]);

const response = await fetch(`https://api.github.com/users/${username}/repos?per_page=100&sort=updated`, {
  headers: {
    Accept: "application/vnd.github+json",
    "User-Agent": "mahmoud-halim-portfolio-importer/1.0",
    "X-GitHub-Api-Version": "2022-11-28",
  },
});

if (!response.ok) {
  throw new Error(`Failed to fetch GitHub repos: ${response.status} ${response.statusText}`);
}

const repos = await response.json();
const projects = repos
  .filter((repo) => !repo.archived)
  .filter((repo) => !excludedRepos.has(repo.name.toLowerCase()))
  .map((repo) => ({
    name: repo.name,
    description: repo.description || "Public GitHub repository.",
    href: repo.html_url,
    language: repo.language || "Code",
    stars: repo.stargazers_count || 0,
    fork: Boolean(repo.fork),
    updatedAt: repo.updated_at,
    tags: [repo.language, ...(repo.topics || [])].filter(Boolean),
  }));

const source = `// Generated by scripts/import-github-repos.mjs. Do not edit by hand.
export const importedProjects = ${JSON.stringify(projects, null, 2)} as const;
`;

await fs.writeFile(outputPath, source);
console.log(`Imported ${projects.length} GitHub repositories into ${outputPath}.`);
